def drawSprite(sprite,x_pos,y_pos):
    t1.up()
    t1.goto(x_pos,y_pos)
    t1.down()
    y_change = 0
    while sprite[y_change] != 9:
        y_change += 1
    y_change+=1
    #print(y_change)
    for x in range(len(sprite)):
        if sprite[x]==0:
            t1.color("white")
            t1.fd(10)
        elif sprite[x]==1:
            t1.color((153,73,0))
            #brown
            t1.fd(10)
        elif sprite[x]==2:
            t1.color((0,150,230))
            #blue
            t1.fd(10)
        elif sprite[x]==3:
            t1.color((255,210,0))
            #lightbrown
            t1.fd(10)
        elif sprite[x]==4:
            t1.color((255,255,75))
            #yellow
            t1.fd(10)
        elif sprite[x]==5:
            t1.color("black")
            #obviously black
            t1.fd(10)
        elif sprite[x]==6:
            t1.color("red")
            #obviously black
            t1.fd(10)
        elif sprite[x]==7:
            t1.color(255,128,0)
            #orange
            t1.fd(10)
        elif sprite[x]==9:
            t1.up()
            t1.goto(x_pos,y_pos+10*(x+1)/y_change)
            t1.down()
